export function generatePrettyUrlName(): string {
  const urlName = [];
  const usedLetters: string[] = [];
  while (urlName.length < 3) {
    // sample from url words.
    const idx = Math.floor(Math.random() * urlWords.length);
    const word = urlWords[idx];
    const firstLetter = word.slice(0, 1);
    if (usedLetters.includes(firstLetter)) {
      continue;
    }
    urlName.push(word);
    usedLetters.push(word.slice(0, 1));
  }

  return urlName.join("-");
}
